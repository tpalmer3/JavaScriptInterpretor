function bubbleSort(list) {
    changed = true;
    while(changed) {
        changed = false;
        for(i = 0; i < list.length-1; i++) {
            if(list[i] > list[i+1]) {
                temp = list[i];
                list[i] = list[i+1];
                list[i+1] = temp;
                changed = true;
            }
        }
    }
    return list;
}

